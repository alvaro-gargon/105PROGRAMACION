    public float getCantidad(){
        float cantidad=-1;
        String entrada;
                      
        entrada=JOptionPane.showInputDialog(this, "Introduzca cantidad");
        if(entrada!=null){
           
            try{
                cantidad=Float.parseFloat(entrada);
            }catch(NumberFormatException nfe){
                mostrarMensaje("Error en la entrada");
            }
           
        }
        return cantidad;
    }
    
