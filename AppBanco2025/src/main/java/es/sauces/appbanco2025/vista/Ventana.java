    public void cambiarVista(TipoUsuario rol){
        if(rol==TipoUsuario.INVITADO){
            this.panelRegistrado.setVisible(false);
            this.bEntrar.setEnabled(true);
            this.bSalir.setEnabled(false);
        }
        else{
            this.panelRegistrado.setVisible(true);
            this.bEntrar.setEnabled(false);
            this.bSalir.setEnabled(true);
       }
        
       pack();
    }
   
  
