    private void bNuevoUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bNuevoUsuarioActionPerformed
        if(this.dialogoUsuario.mostrarCrear()==DialogoUsuario.ACEPTAR){
            mostrarDni(dialogoUsuario.getDni());
            mostrarNombre(dialogoUsuario.getNombre());
            mostrarEmail(dialogoUsuario.getEmail());
            mostrarRol("REGISTRADO");
            controlador.crearUsuario();
        }
    }//GEN-LAST:event_bNuevoUsuarioActionPerformed

    private void bEditarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEditarUsuarioActionPerformed
        if(!getDni().isBlank()){
            dialogoUsuario.mostrarDni(getDni());
            dialogoUsuario.mostrarNombre(getNombre());
            dialogoUsuario.mostrarEmail(getEmail());
            if(dialogoUsuario.mostrarEditar()==DialogoUsuario.ACEPTAR){
                mostrarNombre(dialogoUsuario.getNombre());
                mostrarEmail(dialogoUsuario.getEmail());
                controlador.modificarUsuario();
            }
            
        }
    }//GEN-LAST:event_bEditarUsuarioActionPerformed

    private void bAbrirCuentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAbrirCuentaActionPerformed
        if(!getDni().isBlank()){
            dialogoCuenta.mostrarTitular(getDni());
            if(this.dialogoCuenta.mostrarCrear()==DialogoCuenta.ACEPTAR){
                mostrarCodigoCuenta(dialogoCuenta.getCodigo());
                mostrarSaldoCuenta(dialogoCuenta.getSaldo());
                controlador.abrirCuenta();
            }
        }
    }//GEN-LAST:event_bAbrirCuentaActionPerformed

