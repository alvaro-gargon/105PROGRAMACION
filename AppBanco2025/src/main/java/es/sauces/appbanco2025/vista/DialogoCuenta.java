    private void bIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bIngresarActionPerformed
         if(tfCodigoCuenta.getText().trim().length()!=0 && tfTitular.getText().trim().length()!=0 && this.tfSaldoCuenta.getText().trim().length()!=0){
            opcion=ACEPTAR;
            setVisible(false);
        }
    }//GEN-LAST:event_bIngresarActionPerformed

    private void bCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCancelarActionPerformed
        opcion=CANCELAR;
            setVisible(false);
    }//GEN-LAST:event_bCancelarActionPerformed
public int mostrarCrear(){
        opcion=CANCELAR;
        this.setTitle("Nueva Cuenta");
        this.tfCodigoCuenta.setEnabled(true);
        setVisible(true);
        return opcion;
    }
    
    public int mostrarEditar(){
        opcion=CANCELAR;
        this.setTitle("Editar Cuenta");
        this.tfCodigoCuenta.setEnabled(false);
        setVisible(true);
        return opcion;
    }
    
    public String getCodigo(){
        return this.tfCodigoCuenta.getText();
    }
    
    public String getTitular(){
        return this.tfTitular.getText();
    }
    
    public float getSaldo(){
        return Float.parseFloat(this.tfSaldoCuenta.getText());
    }
    
    public void mostrarCodigo(String codigo){
        this.tfCodigoCuenta.setText(codigo);
    }
    
    public void mostrarTitular(String titular){
        this.tfTitular.setText(titular);
    }
    
    public void mostrarSaldo(float saldo){
        this.tfSaldoCuenta.setText(Float.toString(saldo));
    }
    
