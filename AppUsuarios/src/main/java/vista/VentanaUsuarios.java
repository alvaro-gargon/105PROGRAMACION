    private void buscarBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarBotonActionPerformed
        String nombre = editarTextField.getText();
        if (!nombre.isBlank()) {
            mostrarNombre(nombre);
            controlador.buscar();
        }
    }//GEN-LAST:event_buscarBotonActionPerformed

    private void añadirBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_añadirBotonActionPerformed
        if (this.dialogoUsuario.mostrarCrear() == DialogoUsuario.ACEPTAR) {
            this.nombreTextField.setText(dialogoUsuario.getNombre());
            this.passwordField.setText(dialogoUsuario.getPassword());
            this.emailTextField1.setText(dialogoUsuario.getEmail());
            this.rolComboBox.setSelectedItem(dialogoUsuario.getRol());
            controlador.crear();
        }
    }//GEN-LAST:event_añadirBotonActionPerformed

