    private void menuEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuEditarActionPerformed
        String nombre = editarTextField.getText();
        mostrarNombre(nombre);
        controlador.buscar();
        this.dialogoUsuario.mostrarNombre(getNombre());
        this.dialogoUsuario.mostrarEmail(getEmail());
        this.dialogoUsuario.mostrarRol(getRol());
        if (!this.getEditar().isBlank() && dialogoUsuario.mostrarEditar() == DialogoUsuario.ACEPTAR) {
            this.mostrarEmail(dialogoUsuario.getEmail());
            this.mostrarPassword(dialogoUsuario.getPassword());
            controlador.editar();
            controlador.listar();
        }
    }//GEN-LAST:event_menuEditarActionPerformed

